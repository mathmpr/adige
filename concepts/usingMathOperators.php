<?php

/*
 * em PHP assim como em outras linguagens nos possuímos os operadores básicos
 * + adição, - subtração, / dividir e * multiplicação
 * também possuímos outros operadores como:
 * ** para potência; 2 ** 3 = 8
 * % para obter o resto da divisão; 12 % 5 = 2
 * mais informações podem ser obtidas em: https://www.php.net/manual/pt_BR/language.operators.arithmetic.php
 * quando temos uma string '' ou "" podemos juntar uma com a outra, como uma soma de textos usando um ponto
 * todas as linhas abaixo usam tal método adiciona uma quebra de linha (/n) ao final da linha
 */

echo (2 ** 3)."\n";          // imprime 8
echo (12 % 5)."\n";          // imprime 2
echo (5 % -3)."\n";          // imprime 2
echo (-5 % 3)."\n";          // imprime -2
echo (-5 % -3)."\n";         // imprime -2

